import turtle as tur
def screen_adjustment():
    scr = tur.Screen()
    scr.bgcolor('black')
    tur.title('Iron Maiden')
    tur.penup()
    tur.shape("turtle")
    tur.goto(-650, 100)
    #tur.pencolor((253,239,16,255))
    tur.pen(pencolor='silver' ,fillcolor="black", pensize=5)
    tur.pendown()  
    
def letter_i():
    tur.begin_fill()
    tur.fd(20)
    tur.rt(90)
    tur.fd(150)
    tur.lt(-90)
    tur.fd(20)
    tur.rt(90)
    tur.fd(150)
    tur.end_fill()
    
