    tur.rt(-130)
    tur.fd(30)
    tur.rt(90)
    tur.fd(75)
    tur.lt(-90)
    tur.fd(38)
    tur.lt(95)
    tur.fd(78)
    tur.lt(-90)
    tur.fd(20)
    tur.rt(90)
    tur.fd(120)
    tur.rt(225)
    tur.fd(90)
    tur.rt(90)
    tur.fd(20)
    tur.rt(90)
    tur.fd(145)
    tur.end_fill()
    
